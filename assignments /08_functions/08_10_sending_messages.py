# I will be sending messages to users
def send_message(user, message):
    """Send a message to a user."""
    print(f"Sending message to {user.title()}: {message}")
# Example usage:
send_message("aragnith rusty", "Welcome to our platform!")
send_message("Tereth the great", "Your account has been activated!")
send_message("luna lovegood", "Don't forget to check your notifications!")