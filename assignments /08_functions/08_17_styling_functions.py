def add(a, b):
    """Return the sum of a and b.

    Parameters:
    a (int, float): The first number to add.
    b (int, float): The second number to add.

    Returns:
    int, float: The sum of a and b.

    Examples:
    >>> add(2, 3)
    5
    >>> add(2.5, 3.5)
    6.0
    """
    return a + b